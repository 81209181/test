package com.hkt.btu.sd.facade.data.gmb;

public class GmbVehicleData {

    private String pslId;

    private String regNumber;

    private String routeCode;

    private String contact_name;

    private String contact_tel;

    private String contact_location;

    public String getPslId() {
        return pslId;
    }

    public void setPslId(String pslId) {
        this.pslId = pslId;
    }

    public String getRegNumber() {
        return regNumber;
    }

    public void setRegNumber(String regNumber) {
        this.regNumber = regNumber;
    }

    public String getRouteCode() {
        return routeCode;
    }

    public void setRouteCode(String routeCode) {
        this.routeCode = routeCode;
    }

    public String getContact_name() {
        return contact_name;
    }

    public void setContact_name(String contact_name) {
        this.contact_name = contact_name;
    }

    public String getContact_tel() {
        return contact_tel;
    }

    public void setContact_tel(String contact_tel) {
        this.contact_tel = contact_tel;
    }

    public String getContact_location() {
        return contact_location;
    }

    public void setContact_location(String contact_location) {
        this.contact_location = contact_location;
    }
}
